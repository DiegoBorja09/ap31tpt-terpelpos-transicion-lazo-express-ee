package com.bean;

public class AtributosBono {

    private int IFP;
    private float VFP;
    private String AFP;

    public int getIFP() {
        return IFP;
    }

    public void setIFP(int IFP) {
        this.IFP = IFP;
    }

    public float getVFP() {
        return VFP;
    }

    public void setVFP(float VFP) {
        this.VFP = VFP;
    }

    public String getAFP() {
        return AFP;
    }

    public void setAFP(String AFP) {
        this.AFP = AFP;
    }

}
